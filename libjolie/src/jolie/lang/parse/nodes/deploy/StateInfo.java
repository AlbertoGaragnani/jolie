package jolie.lang.parse.nodes.deploy;

import jolie.Constants;
import jolie.lang.parse.DeployVisitor;

public class StateInfo implements DeploySyntaxNode
{
	private Constants.StateMode mode;
	
	public StateInfo( Constants.StateMode mode )
	{
		this.mode = mode;
	}
	
	public Constants.StateMode mode()
	{
		return mode;
	}
	
	public void accept( DeployVisitor visitor )
	{
		visitor.visit( this );
	}
}
